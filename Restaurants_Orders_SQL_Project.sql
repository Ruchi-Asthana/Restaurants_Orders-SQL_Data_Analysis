/*
-------------------------------------------------------------------------------------
Q1. Display all completed orders along with customer name and restaurant name.
-------------------------------------------------------------------------------------
*/
SELECT 
    o.order_id,
    c.customer_name,
    r.restaurant_name,
    o.order_status
FROM orders o
JOIN customers c 
    ON o.customer_id = c.customer_id
JOIN restaurants r 
    ON o.restaurant_id = r.restaurant_id
WHERE o.order_status = 'completed';

/*
--------------------------------------------------------------------------------------
Q2. Find the total revenue generated by each restaurant.
--------------------------------------------------------------------------------------
*/
SELECT 
    r.restaurant_id,
    r.restaurant_name,
    SUM(o.order_amount) AS total_revenue
FROM restaurants r
JOIN orders o 
    ON r.restaurant_id = o.restaurant_id
WHERE o.order_status = 'completed'
GROUP BY 
    r.restaurant_id,
    r.restaurant_name
ORDER BY 
    total_revenue DESC;

/*
--------------------------------------------------------------------------------------
Q3. Show the total number of orders placed by each customer.
--------------------------------------------------------------------------------------
*/
SELECT 
    c.customer_id,
    c.customer_name,
    COUNT(o.order_id) AS total_orders
FROM customers c
LEFT JOIN orders o
    ON c.customer_id = o.customer_id
GROUP BY 
    c.customer_id,
    c.customer_name
ORDER BY 
    total_orders DESC;
    
/*
--------------------------------------------------------------------------------------
Q4. Find the average order amount for each city.
--------------------------------------------------------------------------------------
*/
SELECT 
    c.city,
    AVG(o.order_amount) AS avg_order_amount
FROM customers c
JOIN orders o
    ON c.customer_id = o.customer_id
WHERE o.order_status = 'completed'
GROUP BY 
    c.city
ORDER BY 
    avg_order_amount DESC;
    
/*
--------------------------------------------------------------------------------------
Q5. Categorize orders as Low, Medium, and High based on order amount.
--------------------------------------------------------------------------------------
*/
SELECT 
    order_id,
    customer_id,
    restaurant_id,
    order_amount,
    CASE 
        WHEN order_amount < 500 THEN 'Low'
        WHEN order_amount BETWEEN 500 AND 1500 THEN 'Medium'
        ELSE 'High'
    END AS order_category
FROM orders
WHERE order_status = 'completed';

/*
--------------------------------------------------------------------------------------
Q6. Display restaurants whose average order value is greater than the overall average order value.
--------------------------------------------------------------------------------------
*/
SELECT 
	r.restaurant_id,
    r.restaurant_name,
    AVG(o.order_amount) AS restaurant_avg_order_value,
    (
        SELECT AVG(order_amount)
        FROM orders
        WHERE order_status = 'completed'
    ) AS overall_avg_order_value
FROM restaurants r
JOIN orders o
    ON r.restaurant_id = o.restaurant_id
WHERE o.order_status = 'completed'
GROUP BY 
    r.restaurant_id,
    r.restaurant_name
HAVING
    AVG(o.order_amount) > (
        SELECT AVG(order_amount)
        FROM orders
        WHERE order_status = 'completed'
    )
ORDER BY restaurant_avg_order_value DESC;

/*
--------------------------------------------------------------------------------------
Q7. Find customers who have never placed any orders.
--------------------------------------------------------------------------------------
*/
SELECT 
    c.customer_id,
    c.customer_name
FROM customers c
LEFT JOIN orders o
    ON c.customer_id = o.customer_id
GROUP BY 
    c.customer_id,
    c.customer_name
HAVING
	count(o.order_id) = 0;
    
/*
--------------------------------------------------------------------------------------
Q8. Show the top 5 customers based on total spending.
--------------------------------------------------------------------------------------
*/
SELECT 
    c.customer_id,
    c.customer_name,
    SUM(o.order_amount) AS total_spending
FROM customers c
JOIN orders o
    ON c.customer_id = o.customer_id
WHERE o.order_status = 'completed'
