/*
--------------------------------------------------------------------------------------
Q9. Find the most frequently used payment method.
--------------------------------------------------------------------------------------
*/
SELECT 
    payment_method,
    COUNT(*) AS usage_count
FROM orders
WHERE order_status = 'completed'
GROUP BY payment_method
ORDER BY usage_count DESC
LIMIT 1;

/*
--------------------------------------------------------------------------------------
Q10. Calculate the running total of revenue by order date.
--------------------------------------------------------------------------------------
*/
SELECT
    order_date,
    SUM(order_amount) AS daily_revenue,
    SUM(SUM(order_amount)) OVER (
        ORDER BY order_date
    ) AS running_total_revenue
FROM orders
WHERE order_status = 'completed'
GROUP BY order_date
ORDER BY order_date;

/*
--------------------------------------------------------------------------------------
Q11. Rank restaurants based on total revenue generated.
--------------------------------------------------------------------------------------
*/
SELECT
    r.restaurant_id,
    r.restaurant_name,
    SUM(o.order_amount) AS total_revenue,
    RANK() OVER (
        ORDER BY SUM(o.order_amount) DESC
    ) AS revenue_rank
FROM restaurants r
JOIN orders o
    ON r.restaurant_id = o.restaurant_id
WHERE o.order_status = 'completed'
GROUP BY 
    r.restaurant_id,
    r.restaurant_name
ORDER BY 
    revenue_rank;
    
/*
--------------------------------------------------------------------------------------
Q12. Find the second highest order amount from the orders table. 
--------------------------------------------------------------------------------------
*/
SELECT DISTINCT order_amount
FROM orders
WHERE order_status = 'completed'
ORDER BY order_amount DESC
LIMIT 1 OFFSET 1;

/*
--------------------------------------------------------------------------------------
Q13. Find customers who placed more orders than the average number of orders per customer.
--------------------------------------------------------------------------------------
*/
SELECT 
    c.customer_id,
    c.customer_name,
    COUNT(o.order_id) AS total_orders,
    (
        SELECT AVG(order_count)
        FROM (
            SELECT COUNT(order_id) AS order_count
            FROM orders
            GROUP BY customer_id
        ) t
    ) AS avg_order_count
FROM customers c
JOIN orders o
    ON c.customer_id = o.customer_id
GROUP BY 
    c.customer_id,
    c.customer_name
HAVING 
    COUNT(o.order_id) > (
        SELECT AVG(order_count)
        FROM (
            SELECT COUNT(order_id) AS order_count
            FROM orders
            GROUP BY customer_id
        ) t
    )
ORDER BY total_orders DESC;

/*
--------------------------------------------------------------------------------------
Q14.Display the top 3 restaurants in each city based on total orders received. 
--------------------------------------------------------------------------------------
*/
SELECT
    city,
    restaurant_id,
    restaurant_name,
    total_orders,
    rank_in_city
FROM (
    SELECT
        r.city,
        r.restaurant_id,
        r.restaurant_name,
        COUNT(o.order_id) AS total_orders,
        DENSE_RANK() OVER (
            PARTITION BY r.city
            ORDER BY COUNT(o.order_id) DESC
        ) AS rank_in_city
    FROM restaurants r
    JOIN orders o
        ON r.restaurant_id = o.restaurant_id
    WHERE o.order_status = 'completed'
    GROUP BY 
        r.city,
        r.restaurant_id,
        r.restaurant_name
) t
WHERE rank_in_city <= 3
ORDER BY city, rank_in_city;

/*
--------------------------------------------------------------------------------------
Q15. Calculate month-wise revenue and show the previous month revenue.
--------------------------------------------------------------------------------------
*/
SELECT
    year(order_date) AS year, month(order_date) AS month,
    SUM(order_amount) AS monthly_revenue,
    LAG(SUM(order_amount)) OVER (
        ORDER BY year(order_date), month(order_date)
    ) AS previous_month_revenue
FROM orders
WHERE order_status = 'completed'
GROUP BY year(order_date), month(order_date)
ORDER BY year(order_date), month(order_date);

